// Idioma atual
let currentLanguage = 'pt';

// Função para trocar idioma
function switchLanguage(lang) {
    currentLanguage = lang;
    updatePageContent();
    
    // Salvar preferência no localStorage
    localStorage.setItem('preferredLanguage', lang);
    
    // Atualizar classe ativa no seletor
    document.querySelectorAll('.language-option').forEach(option => {
        option.classList.remove('active');
    });
    document.querySelector(`.language-option[data-lang="${lang}"]`).classList.add('active');
    
    // Atualizar o texto e bandeira do seletor atual
    const currentSelector = document.querySelector('.language-current');
    const flagMap = {
        'pt': { flag: 'flag-brazil', text: 'PT' },
        'en': { flag: 'flag-usa', text: 'EN' },
        'es': { flag: 'flag-spain', text: 'ES' }
    };
    
    if (currentSelector && flagMap[lang]) {
        const flagElement = currentSelector.querySelector('.language-flag');
        const textElement = currentSelector.querySelector('span');
        
        flagElement.className = `language-flag ${flagMap[lang].flag}`;
        textElement.textContent = flagMap[lang].text;
    }
}

// Função para atualizar o conteúdo da página
function updatePageContent() {
    const translation = translations[currentLanguage];
    
    // Atualizar todos os elementos com data-translate
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translation[key]) {
            element.innerHTML = translation[key];
        }
    });
    
    // Atualizar o título da página
    if (translation['page-title']) {
        document.title = translation['page-title'];
    }
    
    // Atualizar o atributo lang do HTML
    document.documentElement.lang = currentLanguage === 'pt' ? 'pt-BR' : 
                                   currentLanguage === 'en' ? 'en-US' : 'es-ES';
}

// Função para carregar idioma preferido
function loadPreferredLanguage() {
    const saved = localStorage.getItem('preferredLanguage');
    if (saved && translations[saved]) {
        switchLanguage(saved);
    }
}

// Inicializar quando a página carregar
document.addEventListener('DOMContentLoaded', function() {
    loadPreferredLanguage();
});
